	@MATKHAU	NVARCHAR(100) -- Will be inserted as text
)
AS
BEGIN
	DECLARE @Enc_PASSWORD VARBINARY(MAX)
	SET @Enc_PASSWORD = CAST(HASHBYTES('MD5', @MATKHAU) AS VARBINARY(MAX))
