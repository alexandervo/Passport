CREATE OR ALTER PROCEDURE SP_SEL_PUBLIC_NHANVIEN(
		@TENDN NVARCHAR(100),
		@MK VARCHAR(20))
AS
BEGIN
	-- Check pass xem giống không
	DECLARE @PASSWORD VARBINARY(MAX)
    SET @PASSWORD = HASHBYTES('SHA1', @MK)

	SELECT MANV, HOTEN, EMAIL, CONVERT(VARCHAR, DecryptByASymKey(ASYMKEY_ID(MANV), LUONG, Cast(@MK AS NVARCHAR(20)))) AS 'LUONGCB'
	FROM NhanVien WHERE NHANVIEN.TENDN = @TENDN
      AND NHANVIEN.MATKHAU = @PASSWORD
END
GO

