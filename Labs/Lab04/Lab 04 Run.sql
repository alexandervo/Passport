CREATE PROCEDURE SP_INS_PUBLIC_SINHVIEN (
	@MASV		NVARCHAR(20),
	@HOTEN		NVARCHAR(100),
	@NGAYSINH	DATETIME,
	@DIACHI		NVARCHAR(200),
	@MALOP		NVARCHAR(20),
