-- Tạo Procedures nè
CREATE OR ALTER PROCEDURE SP_INS_PUBLIC_NHANVIEN (
	@MANV		NVARCHAR(20),
	@HOTEN		NVARCHAR(100),
	@EMAIL		VARCHAR(20),
	@LUONGCB	VARCHAR(20),    -- trước khi được mã hoá
	@TENDN		NVARCHAR(100),
	@MK			VARCHAR(20)	-- Will be inserted as text
)
AS
BEGIN
	DECLARE @LUONG VARBINARY(MAX);
	DECLARE @PASSWORD VARBINARY(MAX);
	
	-- Encrypt Salary
	-- Create asymkey
	DECLARE @SQL_Statement NVARCHAR(MAX);
	SET @SQL_Statement = 'CREATE ASYMMETRIC KEY ' + QUOTENAME(@MANV) + ' WITH ALGORITHM = RSA_2048 ENCRYPTION BY PASSWORD = ''' + @MK + ''''
	EXECUTE(@SQL_Statement);
	SET @LUONG = EncryptByASymKey(AsymKey_ID(@MANV), @LUONGCB);
	
	-- Encrypt Pass
	SET @PASSWORD = HASHBYTES('SHA1', @MK);

	INSERT INTO NhanVien(MANV, HOTEN, EMAIL, LUONG, TENDN, MATKHAU, PUBKEY)
				VALUES (@MANV, @HOTEN, @EMAIL, @LUONG, @TENDN, @PASSWORD, @MANV)
END
GO

